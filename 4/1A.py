import numpy as np
# A
# Создаём матрицу A
a = np.array([[1, 3, 5, 7, 9, 11], [2, 4, 6, 8, 10, 12]])
# Создаём матрицу B
b = np.array([[1, 2, 8], [3, 4, 7], [5, 6, 9]])
# Транспонируем матрицу A
a = a.transpose()
# Транспонируем матрицу B
b = b.transpose()
# Выводим матрицу А
print(a)
# Выводим матрицу B
print(b)


